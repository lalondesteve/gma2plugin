local getView = function(fixed_number, song_pool_number, pool_number)
	-- song_pool_number needs to replace the pool number for views
	-- for views on the same y values, an x needs to be provided and one of them requires the ha_focus and has_scrollfocus
	-- hence the weird table manipulation.
	-- todo, make this better functions and something more straightforward
	return {
		view0 = {
			type = types.view,
			pool_num = fixed_number,
			y = 7,
			anz_rows = 1,
			anz_cols = 8,
		},
		view1 = {
			type = types.view,
			pool_num = song_pool_number,
			y = 7,
			x = 8,
			anz_rows = 1,
			anz_cols = 8,
			scroll_offset = 3,
		},
		all = { type = types.all, pool_num = pool_number, y = 5, anz_rows = 2, anz_cols = 16 },
		group0 = { type = types.group, pool_num = pool_number, y = 3, anz_rows = 1, anz_cols = 9 },
		group1 = { type = types.group, pool_num = fixed_number, y = 3, x = 9, anz_rows = 1, anz_cols = 7 },
		group2 = { type = types.group, pool_num = pool_number + 8, y = 4, anz_rows = 1, anz_cols = 16 },
		media = { type = types.preset15, pool_num = pool_number, y = 2, anz_rows = 1, anz_cols = 16 },
		effect0 = { type = types.effect, pool_num = pool_number, y = 1, anz_rows = 1, anz_cols = 16 },
		effect1 = { type = types.effect, pool_num = fixed_number, y = 0, anz_rows = 1, anz_cols = 16 },
	}
end

local getWidgetTag = function(index, view)
	local template =
		'<Widget index="%d" type="%s" display_nr="1" %s y="%d" anz_rows="%d" anz_cols="%d" scroll_offset="%d" scroll_index="%d">'
	if view.x == nil then
		view.x = ""
	else
		view.x = string.format('has_focus="true" has_scrollfocus="true" x="%d"', view.x)
	end
	if view.scroll_offset == nil then
		view.scroll_offset = view.pool_num
	else
		-- if we don't want the current pool_num to be the first item of the view widget
		view.scroll_offset = view.pool_num - view.scroll_offset
	end
	local values = { index, view.type, view.x, view.y, view.anz_rows, view.anz_cols, view.scroll_offset, view.pool_num }
	for k, v in pairs(values) do
		gma.feedback(k .. " : " .. v)
	end
	local footer = [[
			<Data>
				<Data>0</Data>
				<Data>0</Data>
				<Data>0</Data>
				<Data>3</Data>
			</Data>
		</Widget>
    ]]
	return string.format(template, table.unpack(values)) .. footer

	--
end

